    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        
        ctrlGraphique = new CtrlGraphique();
        DefaultCategoryDataset donnees = new DefaultCategoryDataset();
        donnees = new DefaultCategoryDataset();
        double nombreDeLecon;
        String mois;
        for (String valeur : ctrlGraphique.GetDatasGraphiqueEleveLecon(eleve.getNumEleve()).keySet())
        {
            nombreDeLecon = ctrlGraphique.GetDatasGraphiqueEleveLecon(eleve.getNumEleve()).get(valeur);
            
            switch (valeur) {
                case "1": 
                    mois = "Janvier";
                    break;
                case "2":
                    mois = "Février";
                    break;
                case "3":
                    mois = "Mars";
                    break;
                case "4":
                    mois = "Avril";
                    break;
                case "5":
                    mois = "Mai";
                    break;
                case "6":
                    mois = "Juin";
                    break;
                case "7":
                    mois = "Juillet";
                    break;
                case "8":
                    mois = "Août";
                    break;
                case "9":
                    mois = "Septembre";
                    break;
                case "10":
                    mois = "Octobre";
                    break;
                case "11":
                    mois = "Novembre";
                    break;
                case "12":
                    mois = "Décembre";
                    break;
                default:
                    throw new AssertionError();
            }
            //mois = valeur;
            donnees.setValue(nombreDeLecon,"",mois);
        }
        JFreeChart chart1 = ChartFactory.createBarChart(
                "Nombre de leçon par mois durant l'année",
                "Mois",
                "Nombre de Leçon",
                donnees,
                PlotOrientation.VERTICAL,false, true, false);
        ChartPanel graph = new ChartPanel(chart1);
        graph = new ChartPanel(chart1);
        pnlGraphMonitteur1.add(graph);
        pnlGraphMonitteur1.validate();
        
    }//GEN-LAST:event_formWindowOpened

